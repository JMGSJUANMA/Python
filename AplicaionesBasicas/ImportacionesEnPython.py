# Importamos el módulo math
import math

x = int(input("Ingrese un número:\n"))
# utilizamos la función sqrt del módulo math

raiz_cuadrada = math.sqrt(x)
# imprimimos el resultado
print("La raíz cuadrada de", x, "es", raiz_cuadrada)

